// Cart functionality
let cart = {
    items: [],
    total: 0,
    userId: '3FA85F64-5717-4562-B3FC-2C963F66AFA6' // Default GUID for testing
};

// API base URL - this will be replaced with the actual URL from appsettings.json
const API_BASE_URL = 'http://localhost:5087';

// Load cart data from API
async function loadCart() {
    try {
        const response = await fetch(`${API_BASE_URL}/api/Cart/${cart.userId}`);
        if (response.ok) {
            const cartItems = await response.json();
            cart.items = cartItems;
            updateCartBadge();
        }
    } catch (error) {
        console.error('Error loading cart:', error);
    }
}

// Modal handling
function openCartModal() {
    const modal = document.getElementById('cart_modal');
    if (modal) {
        modal.showModal();
    }
}

// Update cart badge count
function updateCartBadge() {
    const cartCount = document.getElementById('cartItemCount');
    if (cartCount) {
        cartCount.textContent = cart.items.length;
    }
}

// Add to cart function (called from product page)
async function addToCart(productId) {
    console.log('addToCart function called!');
    try {
        const cartItem = {
            id: '00000000-0000-0000-0000-000000000000', // New GUID will be generated by API
            userId: cart.userId,
            productId: productId,
            quantity: 1
        };

        console.log('Sending cart item:', cartItem);

        const response = await fetch(`${API_BASE_URL}/api/Cart`, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
            },
            body: JSON.stringify(cartItem)
        });

        if (response.ok) {
            // Reload cart data from API
            await loadCart();

            // Show success message
            alert('Product added to cart!');

            // Optionally open the cart modal
            openCartModal();
        } else {
            const errorText = await response.text();
            console.error('Error response:', errorText);
            throw new Error(errorText || 'Failed to add to cart');
        }
    } catch (error) {
        console.error('Error:', error);
        alert('Failed to add product to cart');
    }
}

// Update quantity in cart
async function updateQuantity(productId, change) {
    try {
        const cartItem = cart.items.find(item => item.productId === productId);
        if (!cartItem) return;

        const newQuantity = cartItem.quantity + change;
        if (newQuantity <= 0) {
            await removeItem(productId);
            return;
        }

        const updatedCartItem = {
            id: cartItem.id,
            userId: cart.userId,
            productId: productId,
            quantity: newQuantity
        };

        const response = await fetch(`${API_BASE_URL}/api/Cart`, {
            method: 'PUT',
            headers: {
                'Content-Type': 'application/json',
            },
            body: JSON.stringify(updatedCartItem)
        });

        if (response.ok) {
            await loadCart();
            location.reload();
        } else {
            throw new Error('Failed to update quantity');
        }
    } catch (error) {
        console.error('Error:', error);
        alert('Failed to update quantity');
    }
}

// Remove item from cart
async function removeItem(productId) {
    try {
        const response = await fetch(`${API_BASE_URL}/api/Cart/${cart.userId}/items/${productId}`, {
            method: 'DELETE',
        });

        if (response.ok) {
            await loadCart();
            location.reload();
        } else {
            throw new Error('Failed to remove item');
        }
    } catch (error) {
        console.error('Error:', error);
        alert('Failed to remove item');
    }
}

async function addProduct(product) {
    return await fetch('/api/products', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json',
        },
        body: JSON.stringify(product)
    });
}

// Initialize cart and modal on page load
document.addEventListener('DOMContentLoaded', () => {
    loadCart();

    // Initialize modal close buttons
    const modal = document.getElementById('cart_modal');
    if (modal) {
        const closeButtons = modal.querySelectorAll('button[onclick="cart_modal.close()"]');
        closeButtons.forEach(button => {
            button.onclick = () => modal.close();
        });
    }
});
